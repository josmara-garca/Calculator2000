public class Logica {
    
   
    
    public static int suma (int numA, int numB){
        int resultado = 0;
        resultado = numA + numB;
        return resultado;
    }
    
    public static int resta (int numA, int numB){
        int resultado = 0;
        resultado = numA - numB;
        return resultado;
    }
    
    public static int multiplicacion (int numA, int numB){
        int resultado = 0;
        resultado = numA * numB;
        return resultado;
    }
    
    public static int division (int numA, int numB){
        int resultado = 0;
        resultado = numA / numB;
        return resultado;
    }
}
